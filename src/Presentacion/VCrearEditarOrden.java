    public String getTipoPagoSeleccionada(){
        return this.cboxTipoPago.getSelectedItem().toString();
    }
    
    public String getFormaPagoSeleccionada(){
        return this.cboxFormaPago.getSelectedItem().toString();
    }
    
    public String getProveedorSeleccionada(){
        String idTipo = this.cboxProveedor.getSelectedItem().toString();
        idTipo = idTipo.split("-")[0];
        return idTipo;
    }
    
    public String getCentroSeleccionada(){
        String idTipo = this.cboxCentro.getSelectedItem().toString();
        idTipo = idTipo.split("-")[0];
        return idTipo;
    }
    
    public void setTipoPago(String pago){
        for (int i = 0; i < cboxTipoPago.getItemCount(); i++) {
            if (cboxTipoPago.getItemAt(i).equals(pago)) {
                cboxTipoPago.setSelectedIndex(i);
            }
        }    
    }
    
    public void setFormaPago(String forma){
        for (int i = 0; i < cboxFormaPago.getItemCount(); i++) {
            if (cboxFormaPago.getItemAt(i).equals(forma)) {
                cboxFormaPago.setSelectedIndex(i);
            }
        }  
    }
    
    public void setProveedor(String id, String proveedor){
        for (int i = 0; i < cboxProveedor.getItemCount(); i++) {
            if (cboxProveedor.getItemAt(i).equals(id+"-"+proveedor)) {
                cboxProveedor.setSelectedIndex(i);
            }
        } 
    }
    
    public void setCentro(String id, String centro){
        for (int i = 0; i < cboxCentro.getItemCount(); i++) {
            if (cboxCentro.getItemAt(i).equals(id+"-"+centro)) {
                cboxCentro.setSelectedIndex(i);
            }
        }
    }

