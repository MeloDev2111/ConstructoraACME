    private boolean isValidatedForm(){
       
        if (cboxTipoPago.getSelectedItem()==null) {
            return false;
        }
        
        if (cboxFormaPago.getSelectedItem()==null) {
            return false;
        }
        
        if (cboxProveedor.getSelectedItem()==null) {
            return false;
        }
        
        if (cboxCentro.getSelectedItem()==null) {
            return false;
        }
        
        return true;
    }

