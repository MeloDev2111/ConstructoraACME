    public void iniciar(){
        frame.setContentPane(this);
        frame.setUndecorated(true);
        frame.pack();
        frame.setVisible(true);
        frame.setSize(800,410);
        frame.setLocationRelativeTo(null);
        presentador.establecerTablaRequerimientos();
    }
    
    public void cerrar() {
        this.frame.dispose();
    }
    
    public void setTitulo(String nombre){
        this.lblTitulo.setText("REQUERIMIENTO DEL AREA: "+nombre);
    }
    
    public void setTablaRequerimientos(Object[][] listaRequerimientos){
        String[] cabezera = {"Id","Proyecto", "Etapa", "Estado"};
        this.TblRequerimientos.setModel(new javax.swing.table.DefaultTableModel(
            listaRequerimientos,
            cabezera
        ));
    }
    
    public String getidRequeSeleccionado(){
        int pos = TblRequerimientos.getSelectedRow();
        return TblRequerimientos.getValueAt(pos, 0).toString();
    }
    
    private boolean isSelected(){
        if (TblRequerimientos.getSelectedRow()==-1) {
            msg.errorMsg("DEBE SELECCIONAR UN REQUERIMIENTO");
            return false;
        }
        return true;
    }
    
    

